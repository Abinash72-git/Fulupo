class Product {
  final String? id;
  final String? categoryId;
  final String? name;
  final String? subname;
  final String? image;
  final String? weights;
  final double? currentPrice;
  final double? oldPrice;
  final int? count;

  Product({
    this.id,
    this.categoryId,
    this.name,
    this.subname,
    this.image,
    this.weights,
    this.currentPrice,
    this.oldPrice,
    this.count,
  });
}


